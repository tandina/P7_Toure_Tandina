<template>
  <div>
    <h1>Login</h1>

    <input
    type="email"
    name="email"
    v-model="email"
    placeholder="email" />
    <br>
    <input
    type="password"
    name="password"
    v-model="password"
    placeholder="enter password" />
    <br>
    <button
      @click="register">Login</button>
  </div>
</template>

<script>
import AuthenticationService from '@/services/AuthenticationService'
export default {
  name: 'Register',
  data () {
    return {
      email: '',
      password: ''
    }
  },
  methods: {
    async register () {
      await AuthenticationService.register({
        email: this.email,
        password: this.password
      })
    }
  }
}
</script>

